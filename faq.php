                 <h4 class="panel-title">
                    <a href="#" class="ing">Q: What types of losses are covered by the Client Security Fund?</a>
              </h4>

            </div>
            <div id="question7" class="panel-collapse collapse" style="height: 0px;">
                <div class="panel-body">
                     <h5><span class="label label-primary">Answer</span></h5>

                    <p>The Client Security Fund Committee may reimburse losses resulting from the dishonest conduct of a Connecticut attorney who has died, been adjudged incapable, not competent or mentally ill, been disbarred or suspended from the practice of law, been placed on inactive status by a Connecticut court, resigned from the Connecticut bar, or against whom a judgment has been obtained with respect to the dishonest conduct. "Dishonest conduct" means wrongful acts committed by an attorney, in an attorney-client relationship, in the nature of theft or embezzlement of money or the wrongful taking of money, property or other things of value. It does not include wrongful acts committed in connection with the provision of investment services. Furthermore, a loss ordinarily will not be covered by the fund if the claim is presented more than four years after the loss was discovered or should have been discovered or if the loss was suffered by a close relative, business associate or partner of the attorney. In some cases, the Client Security Fund Committee may require you to pursue other remedies that you may have against the attorney who caused your loss before considering your claim, such as filing a civil lawsuit against the attorney or the attorney's estate.</p>
                </div>
            </div>
