                 <h4 class="panel-title">
                    <a href="#" class="ing">Q:  How are people legally permitted to practice law in Connecticut?</a>
              </h4>

            </div>
            <div id="question3" class="panel-collapse collapse" style="height: 0px;">
                <div class="panel-body">
                     <h5><span class="label label-primary">Answer</span></h5>

                    <p>Legal Practice of Law in Connecticut:
                  The Superior Court regulates the practice of law in Connecticut. There are three ways to practice law legally in Connecticut. (They are more fully described in the Connecticut Practice Book Sections 8 and following.)<br>
     <strong>1. Bar Admission;</strong><br>
     <strong>2. Connecticut recognition of attorney licensed to practice law in another state (Pro Hac Vice);</strong> and,<br>
     <strong>3. Law student certification.</strong></p>
