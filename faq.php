                 <h4 class="panel-title">
                    <a href="#" class="ing">Q:  What is the Client Security Fund?</a>
              </h4>

            </div>
            <div id="question5" class="panel-collapse collapse" style="height: 0px;">
                <div class="panel-body">
                     <h5><span class="label label-primary">Answer</span></h5>

                    <p><strong>The Client Security Fund</strong> is a fund established by the rules of the Connecticut Superior Court to provide reimbursement to individuals who have lost money or property as a result of the dishonest conduct of an attorney practicing law in the State of Connecticut, in the course of the attorney-client relationship. The fund provides a remedy for clients who are unable to obtain reimbursement for their loss from any other source.</p>
                </div>
            </div>
