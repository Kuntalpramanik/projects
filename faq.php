            <div class="panel-heading accordion-toggle question-toggle collapsed" data-toggle="collapse" data-parent="#faqAccordion" data-target="#question4">
                 <h4 class="panel-title">
                    <a href="#" class="ing">Q: What happens after a complaint against a lawyer is filed?</a>
              </h4>

            </div>
            <div id="question4" class="panel-collapse collapse" style="height: 0px;">
                <div class="panel-body">
                     <h5><span class="label label-primary">Answer</span></h5>

                    <p>The procedure for disciplining lawyers is also in the Practice Book, beginning with Section 2-29. In addition, the Statewide Grievance Committee has adopted its own rules of procedure to supplement those found in the practice book.
               The Statewide Bar Counsel reviews your complaint and either sends it to be investigated by a grievance panel or sends it to the Statewide Grievance Committee with a recommendation that it be dismissed for one of the reasons stated below.<br>

        <strong>Some reasons for dismissing complaints:</strong><br>

     only involves a fee dispute which is not clearly excessive or improper;
     no claim of misconduct;<br>
              does not contain specific enough information on which to base an investigation;<br>
              duplicates a complaint already considered and dismissed; or,
              involves personal behavior outside the practice of law.
              Every Judicial District has a grievance panel made up of one lay person and two attorneys who do not have offices in the Judicial District. The panel decides whether there is "probable cause" to believe that the lawyer is guilty of misconduct. The grievance panel may hold a hearing on your complaint as part of their investigation. A copy of the grievance panel's probable cause decision will be sent to you within 120 days of the referral to the panel.<br>

              All determinations of a grievance panel finding that there exists probable cause of misconduct are referred to the Statewide Grievance Committee for further action. The committee conducts a hearing open to the public on probable cause referrals. If the complaint is filed on or after January 1, 2004, a disciplinary counsel will present the case to the Committee at its hearing when probable cause of misconduct is found. If a grievance panel determines that there is no probable cause of misconduct, it will dismiss the complaint. Such a dismissal is a final decision, subject to no further review, unless there is an allegation in the complaint that the attorney committed a crime, in which case a finding of no probable cause will be reviewed by the Statewide Grievance Committee. The Statewide Grievance Committee is a board of 21 members. At least seven of them are not lawyers. The committee makes the final decision on the grievance or complaint.
                        </p>
